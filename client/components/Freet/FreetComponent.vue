<!-- Reusable component representing a single freet and its actions -->
<!-- We've tagged some elements with classes; consider writing CSS using those classes to style them... -->

<template>
  <article class="freet">
    <header>
      <h3 class="author">@{{ freet.author }}</h3>
      <div v-if="$store.state.username === freet.author" class="actions">
        <button v-if="editing" @click="submitEdit">✅ Save changes</button>
        <button v-if="editing" @click="stopEditing">🚫 Discard changes</button>
        <button v-if="!editing" @click="startEditing">✏️ Edit</button>
        <button @click="deleteFreet">🗑️ Delete</button>
      </div>
    </header>
    <div v-if="editing">
      <textarea
        class="content"
        :value="draft"
        @input="draft = $event.target.value"
      />
      <input v-model="visibility" type="radio" name="public" :value="'public'" /><label for="public">Public</label>
      <input v-model="visibility" type="radio" name="friends" :value="'friends'" /><label for="friends">Friends</label>
      <input v-model="visibility" type="radio" name="only me" :value="'only me'" /><label for="only me">Only me</label>
    </div>
    <div v-else>
      <p class="visibility"> visibility: {{ freet.visibility }} </p>
      <p class="content">
        {{ freet.content }}
      </p>
    </div>
    <p class="votes">
      <Vote :item="freet" :model="'freet'" />
    </p>
    <p class="comments">
      <a :href="'/#/freet/' + freet.id"><button>Comments</button></a>
    </p>
    <p class="info">
      <span v-if="freet.dateModified === freet.dateCreated">Posted</span>
      <span v-else>Updated</span> 
      at {{ freet.dateModified }}
      <i v-if="freet.dateModified !== freet.dateCreated">(edited)</i>
    </p>
    <section class="alerts">
      <article
        v-for="(status, alert, index) in alerts"
        :key="index"
        :class="status"
      >
        <p>{{ alert }}</p>
      </article>
    </section>
  </article>
</template>

<script>
import Vote from "@/components/Vote/Vote.vue";

export default {
  name: "FreetComponent",
  components: {
    Vote,
  },
  props: {
    // Data from the stored freet
    freet: {
      type: Object,
      required: true,
    },
  },
  data() {
    return {
      editing: false, // Whether or not this freet is in edit mode
      draft: this.freet.content, // Potentially-new content for this freet
      visibility: this.freet.visibility,
      alerts: {}, // Displays success/error messages encountered during freet modification
    };
  },
  methods: {
    startEditing() {
      /**
       * Enables edit mode on this freet.
       */
      this.editing = true; // Keeps track of if a freet is being edited
      this.draft = this.freet.content; // The content of our current "draft" while being edited
    },
    stopEditing() {
      /**
       * Disables edit mode on this freet.
       */
      this.editing = false;
      this.draft = this.freet.content;
    },
    deleteFreet() {
      /**
       * Deletes this freet.
       */
      const params = {
        method: "DELETE",
        callback: () => {
          this.$store.commit("alert", {
            message: "Successfully deleted freet!",
            status: "success",
          });
        },
      };
      this.request(params);
    },
    submitEdit() {
      /**
       * Updates freet to have the submitted draft content.
       */
      if (this.freet.content === this.draft && this.freet.visibility === this.visibility) {
        const error =
          "Error: Edited freet content or visibility should be different than current freet content or visibility.";
        this.$set(this.alerts, error, "error"); // Set an alert to be the error text, timeout of 3000 ms
        setTimeout(() => this.$delete(this.alerts, error), 3000);
        return;
      }

      const params = {
        method: "PATCH",
        message: "Successfully edited freet!",
        body: JSON.stringify({ content: this.draft, visibility: this.visibility }),
        callback: () => {
          this.$set(this.alerts, params.message, "success");
          setTimeout(() => this.$delete(this.alerts, params.message), 3000);
        },
      };
      this.request(params);
    },
    async request(params) {
      /**
       * Submits a request to the freet's endpoint
       * @param params - Options for the request
       * @param params.body - Body for the request, if it exists
       * @param params.callback - Function to run if the the request succeeds
       */
      const options = {
        method: params.method,
        headers: { "Content-Type": "application/json" },
      };
      if (params.body) {
        options.body = params.body;
      }

      try {
        const r = await fetch(`/api/freets/${this.freet._id}`, options);
        if (!r.ok) {
          const res = await r.json();
          throw new Error(res.error);
        }

        this.editing = false;
        this.$store.commit("refreshFreets");

        params.callback();
      } catch (e) {
        this.$set(this.alerts, e, "error");
        setTimeout(() => this.$delete(this.alerts, e), 3000);
      }
    },
  },
};
</script>

